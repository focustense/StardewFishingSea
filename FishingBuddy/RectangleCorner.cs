    /// </summary>
    TopLeft,

    /// <summary>
    /// The top right.
    /// </summary>
    TopRight,

    /// <summary>
    /// The bottom right.
    /// </summary>
    BottomRight,

    /// <summary>
    /// The bottom left.
    /// </summary>
    BottomLeft,
}
